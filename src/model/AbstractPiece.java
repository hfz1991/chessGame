package model;

public abstract class AbstractPiece {
	private int point;
	private int xC;
	private int yC;
	
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getxC() {
		return xC;
	}
	public void setxC(int xC) {
		this.xC = xC;
	}
	public int getyC() {
		return yC;
	}
	public void setyC(int yC) {
		this.yC = yC;
	}
	
	
}
